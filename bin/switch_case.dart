void main() {
  var nilai = "C";

  switch (nilai) {
    case 'A':
      print("Nilai Anda Sangat Bagus");
      break;
    case 'B':
      print("Nilai Anda Bagus");
      break;
    case 'C':
      print("Nilai Anda Cukup");
      break;
    case 'D':
      print("Nilai Anda Buruk");
      break;
    case 'E':
      print("Nilai Anda Sangat Buruk");
      break;
    default:
      print("Tidak Ada Nilai Seperti Itu");
  }
}
